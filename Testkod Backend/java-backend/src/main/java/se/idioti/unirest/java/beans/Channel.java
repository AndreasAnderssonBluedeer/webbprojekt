package se.idioti.unirest.java.beans;

public class Channel {
	private String name;
	private int id;
	
	public Channel() {
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
}
